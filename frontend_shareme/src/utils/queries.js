// methods
export const getUser = userId =>{
    return new Promise(function (resolve, reject){
        let query = userQuery(userId);
        client.fetch(query).then(data=>{
            resolve(data[0]);
        })
    })
    
}

export const getFeed = ()=>{
    return new Promise(function (resolve, reject){
        let query = feedQuery;
        client.fetch(query).then(data=>{
            resolve(data);
        })
    })
    
}

export const searchPins = key =>{
    return new Promise(function (resolve, reject){
        let query = searchQuery(key);
        client.fetch(query).then(data=>{
            resolve(data);
        })
    })
    
}





